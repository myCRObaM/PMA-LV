package com.example.pma2.Classes;

public class SpinnerSubjectClass {
    Integer id;
    String name;

    public SpinnerSubjectClass(Integer id, String name) {
        this.id = id;
        this.name = name;
    }

    public Integer getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}
